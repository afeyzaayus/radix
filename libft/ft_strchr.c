#include <stddef.h>

char	*ft_strchr(const char *s, int c)
{
	char	find;
	size_t	i;

	i = 0;
	find = (char)c;
	while (s[i])
	{
		if (s[i] == find)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == find)
		return ((char *)s + i);
	return (0);
}
